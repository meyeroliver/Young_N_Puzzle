public class Position
{
    private int i; //not sure yet whether to make this final, bu i dont think so
    private int j;

    public Position ()
    {

    }

    public Position (int i, int j)
    {
        this.i = i;
        this.j = j;
    }

    public int getI()
    {
        return i;
    }

    public int getJ()
    {
        return j;
    }
}
